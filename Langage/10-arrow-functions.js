// function declaration
function hello(name) {
  return `Hello, ${name}!`;
}

// function expression
const greet = function(name) {
  return `Hello, ${name}!`;
};

// arrow function
const greetArrow = (name) => {
  return `Hello, ${name}!`;
};

// arrow function with implicit return
const greetArrowImplicit = (name) => `Hello, ${name}!`;


